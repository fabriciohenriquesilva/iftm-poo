package model;

public class Medico {
	
	private int crm;
	private String nome;
	
	public Medico() { }
	
	public int getCrm() {
		return crm;
	}
	
	public void setCrm(int crm) {
		this.crm = crm;
	}
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
}
